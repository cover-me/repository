import matplotlib.pyplot as plt

def T(R,a):
    return 10.**Tvi(R,a)
    
def Tvi(R,a):
    x = np.log10(R)
    y = a[0]
    for i in range(len(a)-1):
        y = y*x+a[i+1]
    return y

